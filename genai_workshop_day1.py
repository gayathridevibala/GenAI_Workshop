# Import the necessary libraries
from transformers import AutoTokenizer, AutoModelForCausalLM
from transformers import pipeline

MODEL_NAME = "mistralai/Mistral-7B-Instruct-v0.2"

tokenizer = AutoTokenizer.from_pretrained(MODEL_NAME)
model = AutoModelForCausalLM.from_pretrained(
    MODEL_NAME,
    device_map="auto",
    torch_dtype="auto"
)
generator = pipeline(
    "text-generation",
    model=model,
    tokenizer=tokenizer,
    max_new_tokens=200
)

prompt = "Write a short marketing pitch for an AI-powered learning platform."

output = generator(prompt)
print(output[0]["generated_text"])

import gradio as gr
# Create a function to generate text
def generate_text(prompt, max_new_tokens=200):
    # The generator object is already defined in a previous cell
    output = generator(prompt, max_new_tokens=max_new_tokens)
    return output[0]["generated_text"]
